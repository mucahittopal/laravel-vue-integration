    /**
     * @param Request $request
     * @return \Illuminate\Http\RedirectResponse
     */
    public function store(Request $request)
    {
        $request->validate([
            'post_id' => 'required|integer',
            'rate' => 'required|integer|min:1|max:5',
            'text' => 'sometimes|nullable|string|max:150'
        ]);
        $post = Post::whereNotNull('verified_at')->findOrFail($request->post_id);

        if($post->user_id != Auth::user()->id) {
            $create = Auth::user()->reviews()->create($request->all());
            if ($create) {
                $request->session()->flash('success', 'We got your review. Thanks');
            } else {
                $request->session()->flash('error', 'Error!!!');
            }
        }
        return back();
    }

